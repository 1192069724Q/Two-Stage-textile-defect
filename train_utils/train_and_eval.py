def criterion(inputs, target, weight):
    losses = {}
    for name, x in inputs.items():
        # 忽略target中值为255的像素，255的像素是目标边缘或者padding填充
        losses[name] = nn.functional.cross_entropy(x, target, ignore_index=255,weight=weight)

    if len(losses) == 1:
        return losses['out']

    return losses['out'] + 0.5 * losses['aux']

